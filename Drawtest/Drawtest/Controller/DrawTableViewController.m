- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier forIndexPath:indexPath];
    
    switch (self.drawType) {
        case kDrawTableWidth:
        {
            cell.textLabel.text = [NSString stringWithFormat:@"%ld", indexPath.row + 1];
        }
            break;
        case kDrawTableColor:
        {
            cell.backgroundColor = [UIColor colorWithRed:colorRandom green:colorRandom blue:colorRandom alpha:1.000];
        }
            break;
        default:
            break;
    }
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    UITableViewCell *cell = [tableView cellForRowAtIndexPath:indexPath];
    switch (self.drawType) {
        case kDrawTableColor:
            [DrawManager shareInstance].lineColor = cell.backgroundColor;
            break;
        case kDrawTableWidth:
            [DrawManager shareInstance].lineWidth = [cell.textLabel.text floatValue];
        default:
            break;
    }
    [self dismissViewControllerAnimated:YES completion:nil];
}

